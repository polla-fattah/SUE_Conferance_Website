<h1 align="center" style="background:gray"><u>Call for Papers</u></h1>

<p>The 3rd International scientific conference of Salahaddin University-Erbil (SU-ERBIL2011), will be held in Erbil, Kurdistan, IRAQ,  at the Cultural Centre Palace. The conference, through its technical program, will provide a unique opportunity for the academic and industrial community to address new challenges, share solutions and discuss future research directions.</p> 

<p>SU-ERBIL2011 welcomes high quality scientific submissions as well as papers on Scientific and Humanitarian sectors. Expert panel discussions and keynotes will address hot topics and issues in these domains. Topics of interest include, but are not limited to:</p>

