<h3><u>Papers for Review (Draft)</u></h3>
<p>We invite you to submit a paper (not exceeding 10 pages, font: Times New Roman 12, two
column) for the purpose of reviewing by June  1, 2011. All papers are to be submitted in .pdf
or .doc (MS Word2003 or later) version to the email: <a href="mailto:submission@su-erbil2011.org">submission@su-erbil2011.org</a></p>
<p>Submission implies the willingness of at least one of the authors to register and present the paper.
All papers will be peer reviewed by two independent referees of the International Program
Committe of  SU-Erbil 2011. Accepted authors will be invited to submit a final paper.
Notification of acceptance will start on July 15, 2011 - from this date the Registration will be
available.</p>
<h3><u>Final Papers</u></h3>
<p>Authors of accepted papers will be contacted by the publisher of the conference proceedings
directly regarding the publication guideline. Please note the following information:</P>
<ul>
<li>SU-Erbil2011 Authorities will send out the electronic author kit to the communicating
author of each accepted paper. It is the author's responsibility to submit the final version
of the paper at the designated email by the designated date (detailed instructions will be
included in the electronic author kit), 
<li>The Camera Ready full paper is limited to 10 proceeding pages in IEEE format. If your
paper exceeded 10 pages, you have to pay extra page fee for every additional page, 
<li>Each accepted paper should be registered on or before October 5, 2011 by submitting a
registration form. If you have additional paper or extra pages, you have to pay the
required additional fees for your registration to be valid. 
<li>After October 1, 2011 any unregistered paper will be pulled out from the printed
proceedings, 
<li>All relevant information will be available on the conference web page (section
Registration). 
</ul>
